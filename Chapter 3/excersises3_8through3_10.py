places = ['Rome', 'Istanbul', 'Tokyo', 'Berlin']

print("\nHere is a list of places id like to see")
print(places)

print("\nHere is my list sorted alphabetically")
print(sorted(places))


print("\nHere is my list sorted in original order")
print(places)

print("\nHere is my list in reverse order")
places.reverse()
print(places)

print("\nHere is my list in original order")
places.reverse()
print(places)

print("\nHere is my list sorted in alphabetical order")
places.sort()
print(places)

print("\nHere is my list sorted in reverse alphabetical order")
places.sort(reverse=True)
print(places)

